/*
Operadores de comparação
> Maior que
>= Maior ou igual a
< Menor que 
<= Menor ou igual a
== Igualdade
=== Igualdade estrita os tipos tem que ser os mesmos
!= Diferente
!== Diferente estrita os tipos também tem que ser diferentes para dar true
*/

console.log(5 > 10);
console.log(5 >= 10);
console.log(5 < 10);
console.log(5 <= 10);
console.log(5 == 10);
console.log(5 == 5);
console.log(5 === "5");
console.log(5 != 10);
console.log(5 !== 5);